import cv2
import numpy as np
from mss import mss

window_size = (0,0,860,640)
Screenshot_value = mss()

def screenshot():
    img = Screenshot_value.grab(window_size)  # 调用mss的方法截图
    img = np.array(img)  # 转换成numpy数组
    img = cv2.cvtColor(img, cv2.COLOR_BGRA2BGR)  # 图片4通道转3通道
    return img
